<p>Дождевая и талая вода способна причинить серьезные неудобства и даже обрести разрушительных характер в ситуациях затопления цокольных этажей, подвалов, дорожных коммуникаций и фундаментов. Поэтому уже на стадии проектирования желательно внести в план застройки систему поверхностного водоотвода. Это актуально как в массовом строительстве, так и в случае возведения частного коттеджа. Ливневая канализация устанавливается на парковках, автомобильных мойках, складских территориях, в парковой и пешеходной зонах. Предотвращение образования на поверхности грунта, асфальта, плитки или бетона больших луж возможно без нарушения геометрии ландшафта. Можно легко оснастить системой уже эксплуатируемые дворы, дороги, площадки.</p>

<h4>В состав стандартной системы поверхностного водоотвода входят следующие элементы:</h4>

<ul>
	<li>дренажные лотки, принимающие основную нагрузку по отводу осадков, технической воды;</li>
	<li>решетки, выполняющие декоративную и защитную роли (закрывают лотки от мусора, листьев, животных, а также колес автомобилей и ног прохожих);</li>
	<li>пескоуловители – специальные отстойники с возможностью легкого опустошения по мере необходимости (предотвращают забивание ливневой канализации и самих лотков).</li>
</ul>

