<h2>Уклон водоотводных лотков</h2>

<p>Во время монтажа дренажной системы необходимо следить за тем, чтобы лотки шли под определенным углом для естественного стока воды под силой земного притяжения. Угол наклона обычно небольшой – в районе 0,5-1%. Если брать секцию длиной 1 м, то разница высоты между одним и другим концом составит примерно 7-10 мм. Если возникают проблемы с запасом свободного пространства или нет технической возможности выдержать уклон водоотводных лотков по иным причинам, то можно взять лотки с уже выполненным наклоном желоба. То есть секции укладываются на плоской местности, а вода все равно свободно стекает по желобу.</p>
