<h3>Водоотводные лотки своими руками</h3>

<h4>Бетонные, а также композитные секции весят немало и по регламенту профессионального строительства требуют использования спецтехники:</h4>

<ul>
	<li>мини-экскаватора – для рытья траншеи;</li>
	<li>фронтального погрузчика – для засыпки гравийно-песчаной подушки;</li>
	<li>грузовика-манипулятора – для аккуратной выгрузки и укладки железобетонных лотков.</li>
</ul>

<p>Поэтому установить водоотводные лотки своими руками удастся лишь при выборе пластикового варианта. Секции можно доставить даже на легковом автомобиле, а справиться с их укладкой, выравниванием – в одиночку и без специального инструмента. В то же время, специалисты настоятельно рекомендуют изготавливать под лоток бетонное основание или, по крайней мере, выполнять подсыпку из щебня и песка.</p>
