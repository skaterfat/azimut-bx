<h3>Особенности использования колодцев Tegra</h3>

<p>Несмотря на очевидные достоинства колодцев Tegra, для российского потребителя их использование связано с рядом затруднений. Колодцы, выпускаемые Wavin (Польша), имеют высокую стоимость. К тому же, цена может колебаться в зависимости от курса евро, поэтому в ближайшее время ожидать их удешевления не приходится. При изготовлении колодцев на заказ их производство может занять до четырех-пяти недель, а такая задержка приемлема не для всех проектов.</p>

